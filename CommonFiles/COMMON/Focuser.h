class CGEXFocuser
{
public:
	CGEXFocuser ( CWnd * pWnd )
     {
          m_pWnd = pWnd;
     }

     ~CGEXFocuser ()
     {
          m_pWnd -> SetFocus ();
     }

private:
     CWnd * m_pWnd;
};

