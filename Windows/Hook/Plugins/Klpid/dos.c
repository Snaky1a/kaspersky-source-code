{   
    ULONG i, j;

    for (i=0; i <= SrcBufferSize - MyBufferSize; i++)
    {
        for (j = 0; j < MyBufferSize; ++j)
        {
            if ( SrcBuffer[ i + j ] != MyBuffer[ j ] )
            {
                i += j;
                break;
            }
        }
        if (j == MyBufferSize)
            return i;
    }
    return 0;
}


